            <h2>CEMAL SÜREYA</h2>
            </center>
            <p>Cemal Süreya, kimlik adıyla Cemalettin Seber (1931, Erzincan - 9 Ocak 1990, İstanbul), Türk şair, yazar ve çevirmen. Türk şiirinde modernist bir hareket olan İkinci Yeni şiirinin öncü şairlerinden biridir. İlk şiir denemelerini ortaokulda eskizlerle, lisede aruzla yapsa da asıl şiir çalışmaları üniversite yıllarında başlamıştır. Üvercinka (1958), Göçebe (1965), Beni Öp Sonra Doğur Beni (1973), Uçurumda Açan (1984), Sıcak Nal (1988), Güz Bitigi (1988) ve Sevda Sözleri (1990)[not 2] adlarındaki şiir kitaplarının yanı sıra deneme, eleştiri, günlük ve antoloji türlerinde de yazmıştır. Eserlerinde en sık işlediği temalar aşk, kadın, yalnızlık, sosyal ve siyasal eleştiriler, ölüm, tanrı düşüncesi, portreler ve manzum poetikadır. Ayrıca Fransızcadan kırka yakın kitabı Türkçeye çevirmiştir. Onüç Günün Mektupları (1990) dışında hiçbir yazısı veya şiiri, dergi ve gazetede yayımlanmadan kitaba dönüşmemiştir. Sosyalist bir dünya görüşüne sahip olan Süreya, Papirüs dergisini çıkarmış ve bu dergide edebî görüşlerini açıklamasının yanı sıra dergiyi bir aydın olarak fikirlerini ortaya koymak için araç olarak kullanmıştır.

                Dersim İsyanı sebebiyle Erzincan'dan Bilecik'e göç etmek zorunda kalan Alevi Kürt-Zaza bir ailede dünyaya gelen Süreya, devlet memurluğu da yapmıştır. Dört defa evlenen Süreya, bunların dışında "Üvercinka" ve Tomris Uyar dâhil birçok kişiyle ilişkisi olmuştur. Sanat hayatı boyunca çeşitli mahlaslar kullanmış, çocukken kendisine verdiği "Cemal Süreyya" adını kullanırken girdiği bir iddia sonucu adındaki "y" harflerinden birini atmıştır. 9 Ocak 1990'da girdiği şeker koması sonucu ölmüş, cenazesi 11 Ocak'ta Şişli Camii'nde kılınan öğle namazından sonra Kulaksız Mezarlığı'nda toprağa verilmiştir.
                
                Yazdığı kitaplarla 1959'da Yeditepe Şiir Armağanı (Üvercinka), 1966'da Türk Dil Kurumu Şiir Ödülü (Göçebe) ve 1988'de Necatigil Şiir Ödülü (Sıcak Nal ile Güz Bitigi) kazanmıştır. Kitap-lık dergisinin Türkiye'nin kuruluşunun 75. yıldönümü sebebiyle 1998'de hazırladığı "75 Yılda 75 Kitap listesi"nde iki kitabıyla (Üvercinka ve Sevda Sözleri) yer almıştır. Ayrıca çocuklar için ele aldığı yazılardan oluşan ve daha sonra kitaplaştırılan Aritmetik İyi Kuşlar Pekiyi, Türkiye Cumhuriyeti Millî Eğitim Bakanlığı tarafından hazırlanan 100 temel eser listesinde 30. sırada yer almıştır. 1991'den itibaren Cemal Süreya Kültür ve Sanat Derneği tarafından Cemal Süreya Şiir Ödülü verilmektedir.</p>
